    public void readJTabela() {
        DefaultTableModel m = (DefaultTableModel) tabelaPaciente.getModel();
        m.setNumRows(0);
        PacienteDAO pac = new PacienteDAO();

        for (Paciente p : pac.read(txtNomePaciente.getText())) {
            m.addRow(new Object[]{
                p.getNomePaciente(),
                p.getCpf()
            });
        }

    }

