    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (tabelaPaciente.getSelectedRow() != -1) {

            String cpf = (String) tabelaPaciente.getValueAt(tabelaPaciente.getSelectedRow(), 1);
            PacienteDAO pac = new PacienteDAO();
            pac.delete(cpf);

            readJTabela();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Paciente para excluir!");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (tabelaPaciente.getSelectedRow() != -1) {

            String cpf = (String) tabelaPaciente.getValueAt(tabelaPaciente.getSelectedRow(), 1);

            PacienteDAO pac = new PacienteDAO();
            Paciente p = pac.pesquisaPaciente(cpf);

            PrincipalSecretaria.editarCadastro(p);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Paciente para editar!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

